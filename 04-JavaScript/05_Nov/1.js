/* 

Problem Statement 1

Explain the usage of ternary operator with syntax and write a program to check whether the number is even
or odd using the ternary operator.


*/


// Solution 1

// Ternary operator is a conditional operator which takes three operands. It is also known as conditional operator.
// Syntax: condition ? expression1 : expression2
// If the condition is true, the ternary operator returns the value of expression1. Otherwise, it returns the value of expression2.

// Example 1
let a = 10;
let b = 20;
let c = a > b ? a : b;
console.log(c); // 20